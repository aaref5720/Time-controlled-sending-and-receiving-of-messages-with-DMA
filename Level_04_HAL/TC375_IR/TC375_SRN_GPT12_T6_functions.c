/*
 * function:    Func_TC375_SRN_GPT12_T6_init
 * returns:     HAL_Return_Type Error report
 * parameters:  void
 * description: The function initializes the interrupt node SRN GPT120 T6. The
 * interrupt is triggered by GPT12 Timer6. The SRN triggers DMA channel 0
 */

HAL_Return_Type Func_TC375_SRN_GPT12_T6_init(void) {
  HAL_Return_Type tmp;

  /*
   * the Service Request Node (SRN) is initialized
   *
   * field 7..0:   SRPN:   Service Priority Number, here DMA Channel 0
   * field 10:     SRE:    0x0, service request is disabled (will be enabled
   * separately) field 13..11: TOS:    0x1, DMA will receive the request field
   * 25:     CLRR:   0x1, bit is set to 1 to clear service request field 26:
   * SETR:   0x0, bit is cleared, no set request field 28:     IOVCLR: 0x1,
   * clear bit IOV field 30:     SWSCLR: 0x1, clear bit sticky
   *
   */

  SRC_GPT120T6.B.SRPN = T6_SRPN_to_DMA_Ch1;
  SRC_GPT120T6.B.SRE = 0x0;
  SRC_GPT120T6.B.TOS = TOS_DMA;
  SRC_GPT120T6.B.CLRR = 0x1;
  SRC_GPT120T6.B.SETR = 0x0;
  SRC_GPT120T6.B.IOVCLR = 0x1;
  SRC_GPT120T6.B.SWSCLR = 0x1;

  /*
   * SRC_GPT120 is not checked for correct setup
   */

  tmp = HAL_E_OK;

  return tmp;
}

HAL_Return_Type Func_TC375_SRN_GPT12_T2_init(void) {
  HAL_Return_Type tmp;

  SRC_GPT120T2.B.SRPN = T6_SRPN_to_DMA_Ch0;
  SRC_GPT120T2.B.SRE = 0x0;
  SRC_GPT120T2.B.TOS = TOS_DMA;
  SRC_GPT120T2.B.CLRR = 0x1;
  SRC_GPT120T2.B.SETR = 0x0;
  SRC_GPT120T2.B.IOVCLR = 0x1;
  SRC_GPT120T2.B.SWSCLR = 0x1;

  tmp = HAL_E_OK;

  return tmp;
}

HAL_Return_Type Func_TC375_SRN_GPT12_T2_enable(void) {
  HAL_Return_Type tmp = HAL_E_NOT_OK;

  SRC_GPT120T2.B.SRE = 0x1;

  if (SRC_GPT120T2.B.SRE == 0x1) {
    tmp = HAL_E_OK;
  } else {
    tmp = HAL_E_NOT_OK;
  }

  return tmp;
}

HAL_Return_Type Func_TC375_SRN_GPT12_T2_clear(void) {
  HAL_Return_Type tmp = HAL_E_NOT_OK;

  SRC_GPT120T2.B.CLRR = 0x1;

  if (SRC_GPT120T2.B.SRR == 0x0) {
    tmp = HAL_E_OK;
  } else {
    tmp = HAL_E_NOT_OK;
  }

  return tmp;
}

