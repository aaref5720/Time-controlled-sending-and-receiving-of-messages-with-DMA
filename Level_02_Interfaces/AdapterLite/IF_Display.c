/*
 * function:    Func_Adapter_Display_init
 * returns:     L02_Return_Type Error report
 * parameters:  void
 * description: The function initializes the interface to the display on the MF Shield board.
 */

L02_Return_Type Func_Adapter_Display_init(void)
{
    HAL_Return_Type tmp1 = HAL_E_NOT_OK;

    tmp1 = Func_Port_02_Pin_0_init();

    if (tmp1 == HAL_E_OK)
    {
        tmp1 = Func_Port_02_Pin_3_init();

        if (tmp1 == HAL_E_OK)
        {
            tmp1 = Func_Port_02_Pin_4_init();

            if (tmp1 == HAL_E_OK)
            {
                tmp1 = Func_ASCLIN2_init ();

                if (tmp1 != HAL_E_OK)
                {
                    tmp1 = HAL_E_NOT_OK;
                }
            }
            else
            {
                tmp1 = HAL_E_NOT_OK;
            }
        }
        else
        {
            tmp1 = HAL_E_NOT_OK;
        }
    }
    else
    {
        tmp1 = HAL_E_NOT_OK;
    }

    return L02_E_OK;
}


/*********************************************************************************************************************/
/*
 * function:    Func_EncodeDisplayInput
 * returns:     L02_Return_Type Error report
 * parameters:  void
 * description: The function encodes the values to be displayed to a code as it is required by the display.
 */

L02_Return_Type Func_EncodeDisplayInput(DisplayPrompt inputfield[], uint8 outputfield[])
{
    int i;

    for (i = 0; i<4; i++)
    {
        switch (inputfield[i])
        {
            case DispPrompt_0noDot:
                outputfield[i] = 0xC0;
                break;
            case DispPrompt_1noDot:
                outputfield[i] = 0xF9;
                break;
            case DispPrompt_2noDot:
                 outputfield[i] = 0xA4;
                 break;
            case DispPrompt_3noDot:
                 outputfield[i] = 0xB0;
                 break;
            case DispPrompt_4noDot:
                outputfield[i] = 0x99;
                break;
            case DispPrompt_5noDot:
                outputfield[i] = 0x92;
                break;
            case DispPrompt_6noDot:
                 outputfield[i] = 0x82;
                 break;
            case DispPrompt_7noDot:
                 outputfield[i] = 0xF8;
                 break;
            case DispPrompt_8noDot:
                 outputfield[i] = 0x80;
                 break;
            case DispPrompt_9noDot:
                 outputfield[i] = 0x90;
                 break;
            case DispPrompt_ONnoDot:
                 outputfield[i] = 0x80;
                 break;
            case DispPrompt_0withDot:
                outputfield[i] = 0x40;
                break;
            case DispPrompt_1withDot:
                outputfield[i] = 0x79;
                break;
            case DispPrompt_2withDot:
                 outputfield[i] = 0x24;
                 break;
            case DispPrompt_3withDot:
                 outputfield[i] = 0x30;
                 break;
            case DispPrompt_4withDot:
                 outputfield[i] = 0x19;
                break;
            case DispPrompt_5withDot:
                 outputfield[i] = 0x12;
                break;
            case DispPrompt_6withDot:
                 outputfield[i] = 0x02;
                 break;
            case DispPrompt_7withDot:
                 outputfield[i] = 0x78;
                 break;
            case DispPrompt_8withDot:
                 outputfield[i] = 0x00;
                 break;
            case DispPrompt_9withDot:
                 outputfield[i] = 0x10;
                 break;
            case DispPrompt_OFF:
                 outputfield[i] = 0xFF;
                 break;
            case DispPrompt_Dot:
                 outputfield[i] = 0x7F;
                 break;
            default:
                outputfield[i] = 0xFF;
        }
    }

    return L02_E_OK;
}


/*********************************************************************************************************************/
/*
 * function:    Func_BuildDisplayOutput
 * returns:     L02_Return_Type Error report
 * parameters:  void
 * description: The function stores the values to be displayed in a format which is required for the serial transfer.
 */

L02_Return_Type Func_BuildDisplayOutput(uint8 inputfield[], DisplayElementStruct outputfield[])
{
    int i;

    for (i = 0; i < 4; i++)
    {
        outputfield[i].ElementShowCode = inputfield[i];
        outputfield[i].ElementPositionCode = DisplayElementPositionCode[i];
    }

    return L02_E_OK;
}


/*********************************************************************************************************************/
/*
 * function:    Func_Build_ASCLIN_FIFO_Format
 * returns:     L02_Return_Type Error report
 * parameters:  void
 * description: The function stores the values to be displayed in a format which is required by the FIFO of ASCLIN.
 */

L02_Return_Type Func_Build_ASCLIN_FIFO_Format(DisplayElementStruct inputfield[], uint16 outputfield[])
{
    int i;

    for (i = 0; i < 4; i++)
    {
        outputfield[i] = 0x0;
        outputfield[i] = ((inputfield[i].ElementShowCode) << 8)|(inputfield[i].ElementPositionCode);
    }

    return L02_E_OK;
}


/*********************************************************************************************************************/
/*
 * function:    Func_SetDisplayBuffer(DisplayPrompt character, int position)
 * returns:     L02_Return_Type Error report
 * parameters:  DisplayPrompt character: character to be displayed
 *              int position: position of character, allowed range 0..3
 * description: The function stores one character in display input buffer.
 */

L02_Return_Type Func_SetDisplay(DisplayPrompt character, int position)
{
    if((position >= 0) | (position <= 3))
    {
        DisplayInputBuffer[position] = character;
        return L02_E_OK;
    }
    else
    {
        return L02_E_NOT_OK;
    }
}



/*********************************************************************************************************************/
/*
 * function:    Func_Convert2Display
 * returns:     returns:     L02_Return_Type Error report
 * parameters:  void
 * description: The function converts the content of the input buffer to data as required by the display.
 */

L02_Return_Type Func_Convert2Display(void)
{
    L02_Return_Type tmp_L02;

    tmp_L02 = Func_EncodeDisplayInput(DisplayInputBuffer, DisplayInputEncoded);

    if (tmp_L02 == L02_E_OK)
    {
        tmp_L02 = Func_BuildDisplayOutput(DisplayInputEncoded, DisplayOutputBuffer);

        if (tmp_L02 == L02_E_OK)
        {
            tmp_L02 = Func_Build_ASCLIN_FIFO_Format(DisplayOutputBuffer, DisplayOutputFIFO);

            if (tmp_L02 == L02_E_OK)
            {
                return L02_E_OK;
            }
        }
    }
    return L02_E_NOT_OK;
}



