/*
 * The functionalities in this file belong to the Service Interface Layer of the application.
 * The services build the interface to the functions to operate the 4 segment display on the THD AdapterLite.
 *
 * The display is connected to the TC375 using a SPI - like interface.
 *
 * The Interface shall provide the functions:
 * 1) Func_Adapter_Display_init
 *    The function shall initialize the display on the THD adapter.
 *
 * 2) Func_SetDisplayBuffer
 *    The function shall transfer one character into the display input buffer.
 *
 * 3) Func_Convert2Display
 *    The function shall convert the content of the input buffer to data as required by the display.
 *
 * 4) Func_EncodeDisplayInput
 *    The function shall convert characters to code as required by the display.
 *
 * 5) Func_BuildDisplayOutput
 *    The function shall store the values to be displayed in a format which is required for the serial transfer.
 *
 * 6) Func_Build_ASCLIN_FIFO_Format
 *    The function shall store the values to be displayed in a format which is required by the FIFO of ASCLIN.
 *
 *

 */

/*
 * status: V01.0 2025-09-15
 */

