extern HAL_Return_Type Func_Port_02_Pin_0_init (void);
extern HAL_Return_Type Func_Port_02_Pin_3_init (void);
extern HAL_Return_Type Func_Port_02_Pin_4_init (void);

extern HAL_Return_Type Func_ASCLIN2_init (void);

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
